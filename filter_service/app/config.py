"""
Конфигурация Filter Service. Определяет путь к весам модели,
параметры контекста и способ загрузки (CPU/GPU через N_GPU_LAYERS).
"""
from pydantic_settings import BaseSettings, SettingsConfigDict


class Settings(BaseSettings):
    model_config = SettingsConfigDict(env_file=".env", extra="ignore")

    filter_model_path: str
    filter_model_ctx: int = 8192
    filter_model_threads: int = 8

    # Размер батча токенов при обработке промпта (prompt eval). Больший батч
    # ускоряет обработку длинного system-промпта на многоядерном CPU за счёт
    # большего использования SIMD-параллелизма за один проход.
    filter_model_batch: int = 512

    # Верхний предел длины генерации. GBNF-грамматика и так останавливает
    # генерацию сразу после валидного JSON, это доп. предохранитель на случай
    # аномально большого документа с огромным числом сущностей в одном чанке.
    filter_model_max_tokens: int = 1024

    # GBNF-grammar на моделях с большим словарём (Qwen2.5 ~152k токенов)
    # имеет O(vocab_size) стоимость проверки допустимости НА КАЖДОМ шаге
    # генерации — это фиксированная стоимость за токен независимо от контента
    # (~120-150 мс/токен эмпирически измерено на данном железе). Для длинных
    # генераций (несколько сущностей с текстом и обоснованием) это даёт
    # неприемлемые 15-27 сек на короткий чанк. По умолчанию используем
    # НЕконстрained-генерацию (сильный few-shot промпт + мягкий JSON-repair
    # на выходе), что кардинально быстрее. Flag оставлен для демонстрации
    # и сравнения — grammar даёт формальную гарантию структуры ценой скорости.
    filter_use_grammar: bool = False

    # Диагностический режим: включает подробный вывод llama.cpp в stderr,
    # включая разбивку "prompt eval time" / "eval time" в токенах/сек.
    # Используется временно для профилирования производительности,
    # в норме должен быть выключен (verbose-логи засоряют вывод).
    filter_model_verbose: bool = False

    # 0 = чистый CPU, >0 = выгрузка N слоёв на GPU (Vulkan/ROCm сборка)
    n_gpu_layers: int = 0

    log_level: str = "INFO"


settings = Settings()